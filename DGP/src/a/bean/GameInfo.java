package a.bean;

public class GameInfo {
	String acoount;
	String game_name;
	int current_skin;
	int current_box;
	public String getAcoount() {
		return acoount;
	}
	public void setAcoount(String acoount) {
		this.acoount = acoount;
	}
	public String getGame_name() {
		return game_name;
	}
	public void setGame_name(String game_name) {
		this.game_name = game_name;
	}
	public int getCurrent_skin() {
		return current_skin;
	}
	public void setCurrent_skin(int current_skin) {
		this.current_skin = current_skin;
	}
	public int getCurrent_box() {
		return current_box;
	}
	public void setCurrent_box(int current_box) {
		this.current_box = current_box;
	}
	
}
